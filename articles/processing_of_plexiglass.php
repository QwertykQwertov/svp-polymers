        <br><br>Обрабатывать литое оргстекло достаточно легко, однако оно имеет низкую теплопроводность и быстро нагревается. По этой причине лучше высокоскоростная резка небольшими слоями.
        <br><br>Внимание! Чтобы избежать царапания поверхности при хранении и механической обработке, следует сохранять защитную плёнку или бумажное покрытие.
