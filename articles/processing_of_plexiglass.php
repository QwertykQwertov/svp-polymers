        <br><br>Сделать заготовки для выполнения нестандартных деталей, закрепить накладки или создать утолщения можно с помощью сварки. Ее температура для литого акрила может достигать 300 °С. После зачистки шва, деталь подвергают термической обработке для снижения внутренних напряжений.
        <br><br>Листы экструзионного акрила склеивают как между собой, так и с другими материалами. В обоих случаях швы отличаются высокой прочностью и долговечностью. Перед склеиванием рекомендуется проводить термическую обработку.
