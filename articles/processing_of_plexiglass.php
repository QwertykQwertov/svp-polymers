      <h5>Формовка в горячем состоянии</h5>
    </div>
    <div class="card-body">
      <p class="card-text">Такая обработка позволяет придавать оргстеклу практически любую форму. Если результат оказывается неудовлетворительным, деталь можно заново нагреть и отформовать. Литой акрил сохраняет память о предыдущей форме, поэтому при повторном нагревании его легко вернуть в начальное состояние. В отношении экструзионного стекла подобное возможно, только если при изменении формы он не растягивался. С литого пластика перед нагреванием необходимо удалять защитную пленку. На экструзионном акриле ее можно оставить.
        <br><br>Чтобы в готовом изделии не было пузырьков, оргстекло перед формовкой необходимо подвергнуть горячей сушке. Потребность в ней обусловлена тем, что при хранении или транспортировке оргстекло может впитать небольшое количество влаги. Делать это можно либо в конвекционной сушильной камере, либо с помощью инфракрасного излучения. В обоих случаях длительность процедуры составляет 1–2 часа на каждый миллиметр толщины листа при температуре 75–80 °С. Если содержание влаги достаточно велико, продолжительность сушки может увеличиться до 24 часов.
        <br><br>Нагревают литое оргстекло при помощи специального оборудования, позволяющего добиться равномерной температуры по всему объему листа, с разницей не более 3 °С. Нагрев ведется до 165–190 °С и составляет 3–4 минуты на каждый миллиметр толщины.
        <br><br>Поведение при нагреве у литого и экструзионного акрила различно:
      </p>
      <ul>
        <li>Несмотря на быстрое размягчение, прочность у первого практически не снижается по сравнению со вторым.</li>
        <li>Усадка литого оргстекла равномерна и составляет не более 2 %, экструзионного – от 3 до 6 % по длине листа и 1–2 % по ширине (для листа 2 х 3 м).</li>
        <li>Разная реакция на неравномерный прогрев с перепадами в 10–15 °С. Литой акрил выдерживает его достаточно спокойно, на нем не появляются трещины или разрывы при изменении формы. У экструзионного подобный перепад вызывает в нем появление значительных структурных напряжений.</li>
      </ul>
      <p class="card-text">Отличается их поведение и при горячей формовке:
      </p>
      <ul>
        <li>Литой акрил необходимо подвергать значительному давлению для получения желаемой формы.</li>
        <li>Экструзионный акрил не требует особых усилий, для его формовки рекомендуется температура в 160–170 °С, а время нагрева составляет 3 минуты на каждый миллиметр толщины.</li>
      </ul>
      <p class="card-text">Матрицы и пуансоны для создания формованных изделий из акрила изготавливают из дерева, гипса, алюминия и стали. В нагретом состоянии формовка оргстекла происходит следующим образом: деталь из пластика помещают на заготовку, где она под действием собственного веса обретает требуемую форму. Придавать ее акрилу можно и в результате помещения его в вакуумную камеру и последующей обработки в ней, а также путем свободного выдувания сжатым воздухом. Использовать формы в этих случаях необязательно, можно обойтись и без них.
        <br><br>Если необходимо согнуть лист плоского пластика по прямой линии, то можно нагреть его только по оси сгиба. Сделать это легко при помощи одного или нескольких прямолинейных нагревательных элементов, например нихромовой струны.
        <br><br>Внимание! Если переработка или формование ведутся в нагретом состоянии, то защитную пленку необходимо полностью снять и тщательно очистить лист от следов клея. Их остатки при размягчении пластика могут повредить поверхность.
        <br><br>Избежать появления структурного напряжения позволит равномерное длительное охлаждение. Для закрепления новой формы и снижения риска деформации деталь следует оставлять в матрице до достижения ей температуры в 60–70 °С. Формованные изделия перед их дальнейшей обработкой необходимо подвергнуть температурной обработке для снятия внутреннего напряжения.
      </p>
