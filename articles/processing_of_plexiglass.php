      <p class="card-text">Для склеивания как для литого, так и экструзионного оргстекла рекомендуется использовать «родственные» клеи. Они позволяют получить самые прочные и устойчивые к влиянию окружающей среды соединения. Прозрачные бесцветные швы можно сделать, сложив листы внахлест или встык, с использованием клея СММ на основе полиметилметакрилата.
