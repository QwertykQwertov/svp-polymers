        <br><br>Более эффективной будет резка оргстекла на высокоскоростном оборудовании. Сочетание высокой скорости резки и медленной подачи акрила позволяет обеспечить высокое качество кромки. Оптимальный размер диска составляет 250 мм в диаметре. Наиболее подходящим материалом является быстрорежущая сталь, возможно армирование твердым сплавом. Неразведенные зубья позволят избежать деформации оргстекла в месте среза. Рекомендуемая скорость вращения диска циркулярной пилы – 4000 об/мин.
